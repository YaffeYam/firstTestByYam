print ("Adding numbers function:")
def add_numbers(num1, num2):
    result = num1 + num2
    return result

# Example usage:
number1 = 5
number2 = 10
sum_result = add_numbers(number1, number2)

print(f"{number1}+{number2}={sum_result}")
print ("-------------------------------")

print ("Subtracting numbers function:")
def subtract_numbers(num1, num2):
    result = num1 - num2
    return result

# Example usage:
number1 = 10
number2 = 2
sum_result = subtract_numbers(number1, number2)

print(f"{number1}-{number2}={sum_result}")
print ("-------------------------------")